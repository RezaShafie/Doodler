// on "init" you need to initialize your instance
bool HelloWorld::init()
{
    //////////////////////////////
    // 1. super init first
    if ( !Scene::init() )
    {
        return false;
    }

	Lobby* lobby = Lobby::create();

	addChild(lobby);

    return true;
}